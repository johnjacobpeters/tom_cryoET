    curve = 9649;
    offset = 0;
    cyl=tom_cylindermask(ones([box,box,box]),str2double(rad.String),1,[round(box/2),round(box/2)]);
    sph_top=(tom_spheremask(ones([box,box,box]),curve,1,[round(box/2),round(box/2),round(box/2)+offset-round(str2double(height)/2)-curve])-1)*-1;
    sph_bot=(tom_spheremask(ones([box,box,box]),curve,1,[round(box/2),round(box/2),round(box/2)+offset+round(str2double(height)/2)+curve])-1)*-1;
    mask_final=cyl.*sph_top.*sph_bot;
    test = mask_geometric(box,0,str2double(rad.String),str2double(height.String),9649);
    %cyl = tom_cylindermask(, str2double(rad.String),1,[round(box/2),round(box/2)]);
    if exist(newstar)==2
        system(['rm ', name.String]);
    end
    tom_mrcwrite(test, 'name', name.String)
end
if exist('chimera') == 1
    system(['alterheader ',name.String,' -del ',strcat(pxsz.String,',',pxsz.String,',',pxsz.String)]);
elseif exist('chimera') == 0
    disp('Cannot find Chimera. Attempting to source sbgrid')
    system(['source /programs/sbgrid.shrc && alterheader ',name.String,' -del ',strcat(pxsz.String,',',pxsz.String,',',pxsz.String)]);
