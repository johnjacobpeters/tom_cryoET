function varargout = tom_rawreadgui(varargin)
%TOM_RAWREADGUI provides a user interface for TOM_RAWREAD
%
%   varargout = tom_rawreadgui(varargin)
%
%PARAMETERS
%
%  INPUT
%   em_name             filename
%   form                'char', 'short, 'int16', 'int32', 'float', 'double'
%   endian              'le' for little-endian(PC), 'be' for big-endian(SGI,MAC)
%   dims                dimensions
%   byte                block length (currently not used)
%   header              header skippes in bytes
%  
%  OUTPUT
%   Data_read			Image or 3D data
%
%EXAMPLE
%   i=tom_rawread;
%       calls tom_rawreadgui the user interface and reads the
%       file on the variable i.
%
%   i=tom_rawread('recon_data_final.dat','float','le',[200 200 100],0,512);
%       skips 512 bytes header and reads data in float format, big endian type
%       with a dimension of 200x200x100 voxels.
%
%REFERENCES
%
%SEE ALSO
%   TOM_RAWREAD
%
%   created by SN 11/27/02
%   updated by ...
%
%   Nickell et al., 'TOM software toolbox: acquisition and analysis for electron tomography',
%   Journal of Structural Biology, 149 (2005), 227-234.
%
%   Copyright (c) 2004-2007
%   TOM toolbox for Electron Tomography
%   Max-Planck-Institute of Biochemistry
%   Dept. Molecular Structural Biology
%   82152 Martinsried, Germany
%   http://www.biochem.mpg.de/tom

gui_Singleton = 1;
gui_State = struct('gui_Name',       mfilename, ...
                   'gui_Singleton',  gui_Singleton, ...
                   'gui_OpeningFcn', @tom_rawreadgui_OpeningFcn, ...
                   'gui_OutputFcn',  @tom_rawreadgui_OutputFcn, ...
                   'gui_LayoutFcn',  [] , ...
                   'gui_Callback',   []);
if nargin & isstr(varargin{1})
    gui_State.gui_Callback = str2func(varargin{1});
end

if nargout
    [varargout{1:nargout}] = gui_mainfcn(gui_State, varargin{:});
else
    gui_mainfcn(gui_State, varargin{:});
end
% End initialization code - DO NOT EDIT


% --- Executes just before tom_rawreadgui is made visible.
function tom_rawreadgui_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to tom_rawreadgui (see VARARGIN)

% Choose default command line output for tom_rawreadgui
handles.output = hObject;

% Update handles structure
guidata(hObject, handles);

% UIWAIT makes tom_rawreadgui wait for user response (see UIRESUME)
uiwait(handles.figure1);


% --- Outputs from this function are returned to the command line.
function varargout = tom_rawreadgui_OutputFcn(hObject, eventdata, handles)
% varargout  cell array for returning output args (see VARARGOUT);
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Get default command line output from handles structure
varargout{1} = handles.data;
close(gcf)

% --- Executes on button press in pushbutton2.
function ok_button_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
name=get(handles.filename,'String');

Data_type=get(handles.popupmenu3,'String');
Data_type_value=get(handles.popupmenu3,'Value');
type=Data_type(Data_type_value);

Data_byte=get(handles.popupmenu4,'String');
Data_byte_value=get(handles.popupmenu4,'Value');
byte=Data_byte(Data_byte_value);

dimx=str2num(get(handles.edit6,'String'));
dimy=str2num(get(handles.edit8,'String'));
dimz=str2num(get(handles.edit9,'String'));

header=str2num(get(handles.edit7,'String'));

if get(handles.radiobutton4,'Value')==1 endian='le'; else endian='be'; end;

data=tom_rawread(name,char(type),endian,[dimx dimy dimz],byte,header);

handles.data=data;
guidata(hObject, handles);

uiresume(gcf);

